# 3. Business component class
class StoryboardDesigner:
    """Storyboard design business component - self-contained node, template, structure"""
    
    # Input-output mapping configuration
    INPUT_MAPPING = {
        'story_content': ['story', 'story_content', 'story_text', 'content'],
        'scene_descriptions': ['scene', 'scene_descriptions', 'scenes', 'analyzed_scenes'],
        'visual_style': ['visual_style', 'visual_style_suggestion', 'style'],
        'target_duration': ['target_duration'],  # Target total video duration
        'video_duration_per_clip': ['video_duration_per_clip']  # Duration per video clip from model
    }
    
    OUTPUT_MAPPING = {
        'storyboard_frames': 'storyboard',  # Maintain compatibility
        'total_frames': 'frame_count',
        'overall_style': 'final_style'
    }
    
    @classmethod
    def create_node(cls, name: str, task_path: str, **config) -> ChatNode:
        """Factory method: create storyboard design node"""
        node = ChatNode(name, task_path)
        
        # Configure node (template will be created dynamically in custom_run)
        node.output_structure = StoryboardResult
        
        # Custom run logic
        def custom_run(inputs):
            # Input mapping processing
            story_content = cls._map_input(inputs, 'story_content')
            scene_descriptions = cls._map_input(inputs, 'scene_descriptions')
            visual_style = cls._map_input(inputs, 'visual_style')
            
            if not scene_descriptions:
                raise ValueError("No scene descriptions found in inputs")
            
            # Get target duration and video clip duration from inputs
            target_duration = cls._map_input(inputs, 'target_duration')
            if target_duration is None:
                target_duration = 60  # Default to 60 seconds
            else:
                try:
                    target_duration = int(target_duration)
                except (ValueError, TypeError):
                    target_duration = 60
            
            video_duration_per_clip = cls._map_input(inputs, 'video_duration_per_clip')
            if video_duration_per_clip is None:
                video_duration_per_clip = 8.0  # Default to 8 seconds
            else:
                try:
                    video_duration_per_clip = float(video_duration_per_clip)
                except (ValueError, TypeError):
                    video_duration_per_clip = 8.0
            
            # Create dynamic prompt template based on target duration and video clip duration
            dynamic_template = create_story_storyboard_template(target_duration, video_duration_per_clip)
            
            # Format scene descriptions
            if isinstance(scene_descriptions, list):
                scene_text = '\n'.join([f"{i+1}. {scene}" for i, scene in enumerate(scene_descriptions)])
            else:
                scene_text = str(scene_descriptions)
            
            # Call LLM with dynamic template
            result = (dynamic_template | node.chat_model.with_structured_output(node.output_structure)).invoke({
                'story_content': story_content or 'Not provided',
                'scene_descriptions': scene_text,
                'visual_style': visual_style or 'Not specified'
            })
            
            # Output mapping processing  
            output = cls._map_output(result.model_dump())
            return output
        
        node.run = custom_run
        return node
    
    @classmethod
    def _map_input(cls, inputs: dict, target_field: str):
        """Input field mapping"""
        possible_fields = cls.INPUT_MAPPING.get(target_field, [target_field])
        for field in possible_fields:
            if field in inputs and inputs[field]:
                return inputs[field]
        return None
    
    @classmethod  
    def _map_output(cls, result: dict) -> dict:
        """Output field mapping"""
        mapped_result = {}
        for source_field, target_field in cls.OUTPUT_MAPPING.items():
            if source_field in result:
                mapped_result[target_field] = result[source_field]
        
        return {**result, **mapped_result} 


