from langchain_core.tools import BaseTool
from langchain_core.callbacks import CallbackManagerForToolRun
from typing import Optional
import json

class StoryboardDesignInput(BaseModel):
    """Input parameters for storyboard design tool"""
    scene_descriptions: str = Field(description="Scene descriptions or story breakdown")
    visual_style: Optional[str] = Field(default=None, description="Visual style preference (optional)")

class StoryboardDesignerTool(BaseTool):
    """Storyboard design tool - Create detailed storyboards for video production"""
    
    name: str = "storyboard_designer"
    description: str = """
    Professional storyboard design tool for video production. Creates detailed storyboards with:
    - Frame-by-frame scene breakdowns
    - First and last frame image descriptions
    - Video action descriptions
    - Camera movement specifications
    - Duration estimates
    - Transition effects
    
    This tool converts story scenes into production-ready storyboards for image and video generation.
    """
    args_schema: type = StoryboardDesignInput
    
    def _run(
        self,
        scene_descriptions: str,
        visual_style: Optional[str] = None,
        run_manager: Optional[CallbackManagerForToolRun] = None,
    ) -> str:
        """Execute storyboard design"""
        try:
            print(f"🎬 [StoryboardDesignerTool] Starting storyboard design...")
            print(f"🎬 [StoryboardDesignerTool] Scene descriptions: {scene_descriptions[:100]}...")
            
            # Prepare input data
            input_data = {
                'scene_descriptions': scene_descriptions,
                'visual_style': visual_style or "Create freely based on story content"
            }
            
            # Create temporary node for storyboard design
            designer = StoryboardDesigner()
            node = designer.create_node("temp_storyboard_designer", ".")
            
            # Execute storyboard design
            result = node.run(input_data)
            
            # Format storyboard frames for better readability
            frames_summary = []
            if 'storyboard_frames' in result:
                for frame in result['storyboard_frames']:
                    if isinstance(frame, dict):
                        frame_text = f"Frame {frame.get('frame_id', '?')}: {frame.get('scene_summary', '')}"
                        frames_summary.append(frame_text)
                    else:
                        frames_summary.append(str(frame))
            
            # Format output for better readability
            formatted_result = {
                "storyboard_summary": f"Storyboard design completed with {result.get('total_frames', 0)} frames",
                "total_frames": result.get('total_frames', 0),
                "overall_style": result.get('overall_style', 'Not specified'),
                "frames_summary": frames_summary,
                "production_ready": "Yes - includes detailed image and video descriptions",
                "raw_data": result  # Full data for other tools to use
            }
            
            success_msg = f"✅ Storyboard design completed successfully!\n{json.dumps(formatted_result, indent=2, ensure_ascii=False)}"
            print(f"🎬 [StoryboardDesignerTool] Storyboard design completed")
            return success_msg
            
        except Exception as e:
            error_msg = f"❌ Error occurred during storyboard design: {str(e)}"
            print(f"🎬 [StoryboardDesignerTool] {error_msg}")
            return error_msg 