# 1. Output data structure definition
class StoryboardFrame(BaseModel):
    frame_id: int = Field(description="Frame number")
    scene_summary: str = Field(description="Scene summary description")
    first_image_description: str = Field(description="First frame image description, detailed static scene")
    last_image_description: str = Field(description="Last frame image description, detailed static scene")
    video_description: str = Field(description="Video action description, from first to last frame dynamics")
    camera_movement: str = Field(description="Camera movement description")
    duration: str = Field(description="Duration")
    transition: str = Field(description="Transition effect")

class StoryboardResult(BaseModel):
    total_frames: int = Field(description="Total number of frames")
    overall_style: str = Field(description="Overall visual style")
    storyboard_frames: List[StoryboardFrame] = Field(description="Storyboard frames list")

# 2. Dynamic prompt template function
def create_story_storyboard_template(target_duration: int = 60, video_duration_per_clip: float = 8.0) -> ChatPromptTemplate:
    """
    Create story storyboard template with dynamic frame count based on duration
    
    Args:
        target_duration: Target total video duration in seconds (default: 60)
        video_duration_per_clip: Duration of each video clip generated by the model (default: 8.0)
    
    Returns:
        ChatPromptTemplate configured for the target duration
    """
    # Calculate number of frames needed based on video model's clip duration
    num_frames = max(3, min(8, int(target_duration / video_duration_per_clip + 0.5)))  # Round to nearest, clamp to 3-8
    
    # Calculate duration per frame
    duration_per_frame = target_duration / num_frames
    
    duration_per_frame_str = f"{duration_per_frame:.1f}"
    clip_duration_str = f"{video_duration_per_clip:.1f}"
    
    template_text = """
You are a professional storyboard designer and video director. Based on the provided story content and scene analysis, please design a detailed storyboard script.

Original story:
{story_content}

Scene analysis:
{scene_descriptions}

Visual style suggestion:
{visual_style}

**CRITICAL**: Design EXACTLY NUM_FRAMES storyboards (total duration: ~TARGET_DURATION seconds).

IMPORTANT: Each storyboard frame will be converted into a single video clip that is approximately CLIP_DURATION seconds long.
Therefore, allocate approximately DURATION_PER_FRAME seconds per frame.

Please design storyboards according to the following requirements:

1. **Overall Planning**:
   - Create EXACTLY NUM_FRAMES storyboards to match the target duration
   - Establish overall visual style and color tone
   - Each frame should be approximately DURATION_PER_FRAME seconds

2. **Each Storyboard Design**:
   - **Scene Summary**: Concisely summarize what this storyboard expresses
   - **First Frame Description**: Detailed description of static composition, elements, lighting, etc.
   - **Last Frame Description**: Detailed description of the static scene at the end of this storyboard
   - **Video Description**: Describe the dynamic changes from first to last frame
   - **Camera Movement**: Explain camera movement methods (push/pull/pan/tilt, close-up/wide shot, etc.)
   - **Duration**: Should be approximately DURATION_PER_FRAME seconds
   - **Transition Effect**: Connection method with the next storyboard

3. **Image Description Requirements**:
   - First and last frame descriptions should be detailed enough to guide AI image generation
   - Include specific visual elements: character expressions, object details, environmental atmosphere, lighting effects
   - Descriptions should be specific and clear, avoiding abstract concepts
   - Maintain visual consistency of the scenes

4. **Dynamic Description Requirements**:
   - Video descriptions should clearly explain dynamic changes
   - Include character actions, object movements, environmental changes, etc.
   - Consider technical feasibility of video generation

Example format:
- First frame: A character stands at the scene entrance, warm light illuminating their features, background setting clearly established with rich environmental details
- Last frame: The character moves deeper into the scene, lighting shifts to reflect the changing mood, key visual elements transform to show progression
- Video: The character moves through the scene with purposeful action, camera follows smoothly, environmental details shift naturally, lighting transitions to match the emotional arc
"""
    # Use .replace() for numeric values to avoid f-string escaping issues with ChatPromptTemplate's {braces}
    template_text = (template_text
        .replace('NUM_FRAMES', str(num_frames))
        .replace('TARGET_DURATION', str(target_duration))
        .replace('CLIP_DURATION', clip_duration_str)
        .replace('DURATION_PER_FRAME', duration_per_frame_str))
    
    return ChatPromptTemplate.from_template(template_text)

