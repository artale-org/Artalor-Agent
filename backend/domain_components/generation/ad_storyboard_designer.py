from langchain_core.tools import BaseTool
from langchain_core.callbacks import CallbackManagerForToolRun
from typing import Optional
import json

class AdStoryboardDesignInput(BaseModel):
    """Input parameters for advertisement storyboard design tool"""
    hook: str = Field(description="Advertisement opening hook")
    main_content: str = Field(description="Advertisement main content")
    call_to_action: str = Field(description="Advertisement call to action")
    visual_notes: Optional[str] = Field(default=None, description="Visual presentation notes (optional)")
    product_category: Optional[str] = Field(default=None, description="Product category (optional)")
    visual_style: Optional[str] = Field(default=None, description="Visual style preference (optional)")
    selling_points: Optional[str] = Field(default=None, description="Key selling points (optional)")
    color_palette: Optional[str] = Field(default=None, description="Color palette (optional)")

class AdStoryboardDesignerTool(BaseTool):
    """Advertisement storyboard design tool - Create detailed ad storyboards for video production"""
    
    name: str = "ad_storyboard_designer"
    description: str = """
    Professional advertisement storyboard design tool. Creates detailed ad storyboards with:
    - Opening scene storyboards
    - Product showcase storyboards
    - Lifestyle demonstration storyboards
    - Ending/call-to-action storyboards
    - Frame-by-frame image descriptions
    - Video action descriptions
    - Camera movements and transitions
    - Text overlay specifications
    
    This tool creates production-ready storyboards optimized for advertisement video creation.
    """
    args_schema: type = AdStoryboardDesignInput
    
    def _run(
        self,
        hook: str,
        main_content: str,
        call_to_action: str,
        visual_notes: Optional[str] = None,
        product_category: Optional[str] = None,
        visual_style: Optional[str] = None,
        selling_points: Optional[str] = None,
        color_palette: Optional[str] = None,
        run_manager: Optional[CallbackManagerForToolRun] = None,
    ) -> str:
        """Execute advertisement storyboard design"""
        try:
            print(f"📺 [AdStoryboardDesignerTool] Starting ad storyboard design...")
            print(f"📺 [AdStoryboardDesignerTool] Hook: {hook[:50]}...")
            
            # Prepare input data
            input_data = {
                'hook': hook,
                'main_content': main_content,
                'call_to_action': call_to_action,
                'visual_notes': visual_notes or "Not provided",
                'product_category': product_category or "Not specified",
                'visual_style': visual_style or "Not specified",
                'selling_points': selling_points or "Not specified",
                'color_palette': color_palette or "Not specified"
            }
            
            # Create temporary node for ad storyboard design
            designer = AdStoryboardDesigner()
            node = designer.create_node("temp_ad_storyboard_designer", ".")
            
            # Execute storyboard design
            result = node.run(input_data)
            
            # Format storyboard frames for better readability
            frames_summary = []
            if 'storyboard_frames' in result:
                for frame in result['storyboard_frames']:
                    if isinstance(frame, dict):
                        frame_text = f"Frame {frame.get('frame_id', '?')} ({frame.get('scene_type', 'Unknown')}): {frame.get('duration', 'N/A')}"
                        frames_summary.append(frame_text)
                    else:
                        frames_summary.append(str(frame))
            
            # Format output for better readability
            formatted_result = {
                "storyboard_summary": f"Advertisement storyboard design completed with {result.get('total_frames', 0)} frames",
                "total_frames": result.get('total_frames', 0),
                "total_duration": result.get('total_duration', 'Not specified'),
                "visual_theme": result.get('visual_theme', 'Not specified'),
                "frames_summary": frames_summary,
                "production_ready": "Yes - includes detailed image and video descriptions for each frame",
                "raw_data": result  # Full data for other tools to use
            }

            try:
                scene_groups = {}
                for frame in result.get('storyboard_frames', []):
                    scene_type = frame.get('scene_type', 'unknown') if isinstance(frame, dict) else getattr(frame, 'scene_type', 'unknown')
                    scene_groups.setdefault(scene_type, []).append(frame)

                storyboards = []
                for idx, (scene_type, frames) in enumerate(scene_groups.items(), 1):
                    storyboards.append({
                        "storyboard_id": idx,
                        "scene_type": scene_type,
                        "frames": frames,
                        "frame_count": len(frames)
                    })

                formatted_result["storyboards"] = storyboards
                formatted_result["total_storyboards"] = len(storyboards)
            except Exception as _:
                formatted_result["storyboards"] = [
                    {
                        "storyboard_id": 1,
                        "scene_type": "combined",
                        "frames": result.get('storyboard_frames', []),
                        "frame_count": result.get('total_frames', 0)
                    }
                ]
                formatted_result["total_storyboards"] = 1
            # ----------------------------------------------------------------------

            # Return pure JSON string (no status prefix) so that downstream tools can parse without regex.
            print("📺 [AdStoryboardDesignerTool] Storyboard design completed")
            return json.dumps(formatted_result, ensure_ascii=False)
            
        except Exception as e:
            error_msg = f"❌ Error occurred during ad storyboard design: {str(e)}"
            print(f"📺 [AdStoryboardDesignerTool] {error_msg}")
            return error_msg 